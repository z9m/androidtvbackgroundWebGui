function changeLanguage(lang) {
    if (!translations[lang]) return;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
                el.placeholder = translations[lang][key];
            } else {
                // Preserve icons if they exist in innerHTML but not in translation
                // Simple replacement for now as keys map to full text
                el.innerText = translations[lang][key];
            }
        }
    });
    localStorage.setItem('editor_language', lang);
}

function detectBrowserLanguage() {
    const lang = navigator.language || navigator.userLanguage;
    const shortLang = lang.split('-')[0].toLowerCase();
    return translations[shortLang] ? shortLang : 'en';
}

document.addEventListener('DOMContentLoaded', () => {
    let savedLang = localStorage.getItem('editor_language');
    if (!savedLang) savedLang = detectBrowserLanguage();
    if (!translations[savedLang]) savedLang = 'en';

    const sel = document.getElementById('languageSelect');
    if(sel) {
        sel.value = savedLang;
        changeLanguage(savedLang);
    }
});
